//在汉字库中查找
extern INT16U FindCnCharIndex(INT16U code)
{
	INT16U low,mid,high;

	high=CN_WORD_NUM;
	low=0;
	high--;
	while(low<=high)
	{
		mid=(low+high)/2;
		if(code<g_WordStock[mid].code)
			high=mid-1;
		else if(code>g_WordStock[mid].code)
			low=mid+1;
		else
			return (mid);
	}
	return NOFIND;
}
