#include "includes.h"

/********************************************************************************************************
*                                               CONSTANTS
********************************************************************************************************/

#define TASK_STK_SIZE		64      	   	   		   /* Size Of Each Task's Stacks (# Of WORDs)      */

/********************************************************************************************************
*                                               VARIABLES
********************************************************************************************************/

static OS_STK   MainTaskStk	[TASK_STK_SIZE]; 
/********************************************************************************************************
*                                				TaskStart 
********************************************************************************************************/
extern void MainTask(void *pdata); 

/********************************************************************************************************
* 								 				  Main
********************************************************************************************************/

int main (void)
{
	OSInit();										   /* Initialize uC/OS-II  		                   */
	OSTaskCreate(MainTask, (void *)0, &MainTaskStk[TASK_STK_SIZE - 1], 0);
	OSStart();										   /* Start Multitasking      		               */
}

