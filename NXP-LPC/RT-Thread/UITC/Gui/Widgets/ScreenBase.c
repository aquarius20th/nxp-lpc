void SetScreenInfo(CScreenBase *pScreen,INT8U ParentID,INT8U CurrentID,INT16U nScreenName )
{
	if (pScreen)
	{
		pScreen->ParentID = ParentID;
		pScreen->CurrentID = CurrentID;
		pScreen->ScreenName = nScreenName;
	}
}
  
/*********************************************************************************************************
** 函数名称: CreateScreen
** 函数名称: CreateScreen
**
** 功能描述：  动态创建一个屏幕 从堆中创建
**
** 输　入:  INT32U nSize
** 输　入:  scr_ctor ctor
** 输　入:  scr_dtor dtor
**          
** 输　出:   CScreenBase *
**         
** 全局变量:  
** 调用模块: 无
**
** 作　者:  LiJin
** 日　期:  2009年11月12日
** 备  注:  
**-------------------------------------------------------------------------------------------------------
** 修改人:
** 日　期:
** 备  注: 
**------------------------------------------------------------------------------------------------------
********************************************************************************************************/
CScreenBase * CreateScreen( INT32U nSize,scr_ctor ctor,scr_dtor dtor )
{
	CScreenBase *pObj;

	if (nSize <= 0 || nSize > MAX_SCR_SIZE)
		return RT_NULL;

	pObj = rt_malloc(nSize);
	if (pObj == RT_NULL)
		return RT_NULL;

	pObj->Ctor = ctor;
	pObj->Dtor = dtor;
	pObj->Size = nSize;

	if(ctor)
	{
		ctor(pObj);
	}
	return pObj;
}
