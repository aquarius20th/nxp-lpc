void InitScreen(CScreenBase *pScr, scr_ctor ctor,scr_dtor dtor )
{
	ASSERT(pScr);
	if(pScr)
	{
		pScr->Ctor = ctor;
		pScr->Dtor = dtor;
		pScr->Size = 0; //静态

		if(ctor)
		{
			ctor(pScr);
		}
	}
	return;
}
